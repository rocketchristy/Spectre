    def update_quantity(self, cart_items_id, quantity):
        """
        Update the quantity of an existing cart item.
        
        Inputs:
            cart_items_id (int): Cart item's unique identifier
            quantity (int): New quantity value
        
        Outputs:
            dict: {"status": "success"} if quantity updated
                  {"status": "error", "reason": "Order not found"} if item doesn't exist
                  {"status": "error", "reason": str} on other failure
        
        Side Effects:
            Updates QUANTITY field in CART_ITEMS table
            Commits transaction on success, rolls back on failure
        
        Notes:
            Used when user changes quantity of item already in cart
        """
        conn = self.pool.get_connection()
        try:
            sql = """ UPDATE USER01.CART_ITEMS
                    SET QUANTITY = ?
                    WHERE ID = ?  
                    """
            stmt = ibm_db.prepare(conn, sql)
            ibm_db.bind_param(stmt, 1, quantity)
            ibm_db.bind_param(stmt, 2, cart_items_id)
            ibm_db.execute(stmt)
            num_rows = ibm_db.num_rows(stmt)
        
            if num_rows == 0:
                return {"status": "error", "reason": "Cart item not found"}
            
            ibm_db.commit(conn)
            return {"status": "success"}
        except Exception as e:
            ibm_db.rollback(conn)
            return {"status": "error", "reason": str(e)}
        finally:
            self.pool.return_connection(conn)